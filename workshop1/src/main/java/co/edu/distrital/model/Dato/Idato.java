package model.Dato;

/**
 * Interfaz que define un contrato para obtener un valor de tipo long.
 * 
 * Las clases que implementen esta interfaz deben proporcionar una implementación
 * del método {@code getLong()}, que retorna un valor long representativo del objeto.
 * @autor Nicolas Diaz Salamanca <jndiazs@udistrital.edu.co>
 */
public interface Idato {

  public abstract long getLong();   
} 
